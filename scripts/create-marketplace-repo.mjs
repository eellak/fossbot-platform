#!/usr/bin/env node

import { access, copyFile, mkdir, readdir, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { loadLocalEnv } from './load-local-env.mjs'

loadLocalEnv()

const sourceRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const repoName = process.env.FOSSBOT_MARKETPLACE_REPO || 'fossbot-marketplace'
const target = resolve(process.argv[2] || process.env.FOSSBOT_MARKETPLACE_LOCAL_PATH || `../${repoName}`)

if (process.argv.includes('--help') || process.argv.includes('-h')) {
  console.log(`Usage: node scripts/create-marketplace-repo.mjs [target]\n\nScaffolds a local marketplace repository.\nTarget defaults to FOSSBOT_MARKETPLACE_LOCAL_PATH or ../FOSSBOT_MARKETPLACE_REPO.\nSee docs/create-marketplace-repository.md for the complete setup workflow.`)
  process.exit(0)
}

async function assertEmptyTarget() {
  try {
    await access(target)
    const entries = await readdir(target)
    if (entries.length) throw new Error(`Target directory is not empty: ${target}`)
  } catch (error) {
    if (error?.code !== 'ENOENT') throw error
  }
}

const validateWorkflow = `name: Validate marketplace PR

on:
  pull_request:
    paths:
      - 'stages/**'
      - 'scripts/build-marketplace-index.mjs'

permissions:
  contents: read
  checks: write

jobs:
  validate:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: 22
      - run: node scripts/build-marketplace-index.mjs . --validate-stage-repos --check-run --fail-on-error --changed-stage-files
`

const buildWorkflow = `name: Build marketplace index

on:
  push:
    branches: [main]
    paths:
      - 'stages/**'
      - 'scripts/build-marketplace-index.mjs'
  workflow_dispatch:

permissions:
  contents: write
  checks: write

jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-node@v4
        with:
          node-version: 22
      - run: node scripts/build-marketplace-index.mjs . --validate-stage-repos --write-entry-validation --check-run
      - name: Commit generated index
        run: |
          git config user.name "github-actions[bot]"
          git config user.email "41898282+github-actions[bot]@users.noreply.github.com"
          git add index.json stages
          git diff --cached --quiet || git commit -m "chore: rebuild marketplace index"
          git push
`

const readme = `# FOSSBot Stage Marketplace

This repository stores reviewed FOSSBot stage metadata and the generated public index.

- \`stages/<owner>/<repo>.json\` contains one published stage entry.
- \`index.json\` is generated by CI and consumed by FOSSBot Platform.
- Pull requests validate pinned public stage revisions before review.

Do not edit \`index.json\` manually.
`

await assertEmptyTarget()
await Promise.all([
  mkdir(resolve(target, '.github/workflows'), { recursive: true }),
  mkdir(resolve(target, 'scripts'), { recursive: true }),
  mkdir(resolve(target, 'stages'), { recursive: true }),
])
await Promise.all([
  copyFile(resolve(sourceRoot, 'scripts/build-marketplace-index.mjs'), resolve(target, 'scripts/build-marketplace-index.mjs')),
  writeFile(resolve(target, '.github/workflows/validate-marketplace.yml'), validateWorkflow),
  writeFile(resolve(target, '.github/workflows/build-index.yml'), buildWorkflow),
  writeFile(resolve(target, 'index.json'), `${JSON.stringify({ generatedAt: null, schemaVersion: 1, stages: [] }, null, 2)}\n`),
  writeFile(resolve(target, 'stages/.gitkeep'), ''),
  writeFile(resolve(target, 'README.md'), readme),
])

console.log(`Marketplace repository scaffolded at ${target}`)
